    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        Connection conn = null;
        try {
            conn = Conexao.getConnection();
            conn.setAutoCommit(false);
            String select = "select saldo from conta where idconta = ?";
            PreparedStatement ps = conn.prepareStatement(select);
            ps.setInt(1, Integer.valueOf(txtDeb.getText()));
            ResultSet rs = ps.executeQuery();
            
            double valor = Double.valueOf(txtValor.getText());
            
            if (rs.next() && rs.getDouble("saldo")>= valor){
                
                String update = "update conta set saldo = saldo - ? where idconta=?";
                ps = conn.prepareStatement(update);
                
                ps.setDouble(1, valor);
                ps.setInt(2, Integer.valueOf(txtDeb.getText()));
                
                ps.executeUpdate();
                
                
                
                update = "update conta set saldo = saldo + ? where idconta=?";
                ps = conn.prepareStatement(update);
                
                ps.setDouble(1, valor);
                ps.setInt(2, Integer.valueOf(txtCred.getText()));
                
                ps.executeUpdate();
                
                
                conn.commit();
                
                
                
            } else {
                JOptionPane.showMessageDialog(null, "Vc ta liso, n√£o possui saldo!!", 
                        "", JOptionPane.ERROR_MESSAGE);
            }
            
            
            
        } catch (Exception e){
            e.printStackTrace();
            try {
                conn.rollback();
            } catch (SQLException ex) {
                Logger.getLogger(ContaTransacao.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

