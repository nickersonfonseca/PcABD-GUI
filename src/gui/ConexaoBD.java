    private void btInserirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btInserirActionPerformed

        try {

            Class.forName("com.mysql.jdbc.Driver");
            // 

            Connection conn = DriverManager.getConnection(
                    "jdbc:mysql://localhost:3306/aulas", "root", "");

            Statement stm = conn.createStatement();

            String sql = "insert into pessoas (nome, email) values ("
                    +  "'" + txtNome.getText()
                    + "', '" + txtEmail.getText() + "') ";

            
            
            int num = stm.executeUpdate(sql, Statement.RETURN_GENERATED_KEYS);
            
            ResultSet rr = stm.getGeneratedKeys();
            System.out.println(rr.getMetaData().getColumnName(1));
            if (rr.next()){
                System.out.println("I: " + rr.getInt(1));
            }
            
            if (num > 0) {
                JOptionPane.showMessageDialog(null, "Objeto inserido com sucesso!", "", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(null, "Não foi possível inserir o objeto.", "", JOptionPane.ERROR_MESSAGE);
            }

            stm.close();
            conn.close();
            
        } catch (Exception e) {

            System.out.println("ERRO: " + e.getMessage());

        }


    }//GEN-LAST:event_btInserirActionPerformed

