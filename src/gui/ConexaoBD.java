    private void btAltActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAltActionPerformed
        // TODO add your handling code here:

        try {

            Class.forName("com.mysql.jdbc.Driver");
            //

            Connection conn = DriverManager.getConnection(
                "jdbc:mysql://localhost:3306/aulas", "root", "");

            String sql = "update pessoas set nome = ?, email = ? where id = ?";

            PreparedStatement ps = conn.prepareStatement(sql);
            ps.setString(1, txtNome.getText());
            ps.setString(2, txtEmail.getText());
            ps.setInt(3, Integer.valueOf(txtId.getText()));

            int num = ps.executeUpdate();
            if (num > 0) {
                JOptionPane.showMessageDialog(null, "Objeto alterado com sucesso!", "", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(null, "Não foi possível alterar o objeto.", "", JOptionPane.ERROR_MESSAGE);
            }

        } catch (Exception e) {

            System.out.println("ERRO: " + e.getMessage());

        }

    }//GEN-LAST:event_btAltActionPerformed

    private void btSelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSelActionPerformed
        // TODO add your handling code here:

        try {

            Class.forName("com.mysql.jdbc.Driver");
            Connection conn = DriverManager.getConnection(
                "jdbc:mysql://localhost:3306/aulas", "root", "");
            Statement stm = conn.createStatement();

            // comando SQL para selecionar todas as colunas da tabela pessoas
            // onde o nome possua a palavra FERREIRA em qualquer parte
            String sql = "select * from pessoas";
            ResultSet rs = stm.executeQuery(sql); // executa o comando SQL

            DefaultTableModel modelo = (DefaultTableModel) tabela.getModel();
            while (rs.next()) {

                String[] linha = new String[]{rs.getString("id"),
                    rs.getString("nome"), rs.getString("email")};
                modelo.addRow(linha);  // adiciona a linha na JTable

            }

            rs.close();
            stm.close();
            conn.close();

        } catch (Exception e) {

            System.out.println("ERRO: " + e.getMessage());

        }

    }//GEN-LAST:event_btSelActionPerformed

